// 边线点对应数量，平面点对应数量
int corner_correspondence = 0, plane_correspondence = 0;

constexpr double SCAN_PERIOD = 0.1;                         // 雷达扫描频率10hz
constexpr double DISTANCE_SQ_THRESHOLD = 25;                // KD TREE临近搜索阈值
constexpr double NEARBY_SCAN = 2.5;                         // 搜索特征对应scan id阈值

